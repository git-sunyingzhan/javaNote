package basic.day03;

public class OperatorDemo07 {
    public static void main(String[] args) {
        // 左移
        // 0000 0011
        // 0000 1100
        System.out.println(3 << 3); // 3*2^3

        // 有符号右移
        // 1000 0000 0000 0000 0000 0110 0100 原码
        // 1111 1111 1111 1111 1111 1001 1011 反码
        // 1111 1111 1111 1111 1111 1001 1100 补码
        // 1111 1111 1111 1111 1111 1110 0111 >>2 补码
        // 1111 1111 1111 1111 1111 1110 0110 反码
        // 1000 0000 0000 0000 0000 0001 1001 原码

        System.out.println(-100 >> 2); // -100/2^2

        // 无符号右移
        // 1111 1111 1111 1111 1111 1001 1100 补码
        // 0011 1111 1111 1111 1111 1110 0111 >>>2 补码
        System.out.println(-100 >>> 2); // -25

        // 位与
        // 0000 0001
        // 0000 0010
        // 0000 0000
        System.out.println(1 & 2); // 0

        // 位或
        // 0000 0011
        // 0000 0100
        // 0000 0111
        System.out.println(3 & 4); // 7

        // 异或
        // 0000 0101
        // 0000 0110
        // 0000 0011
        System.out.println(5 ^ 6); // 3

        // 取反
        // 0000 0010
        // 1111 1101 补码
        // 1111 1100 反码
        // 1000 0011 原码
        System.out.println(~2); // -3
    }
}
